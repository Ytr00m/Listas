num1 = int(input("Digite o primeiro numero: "))
num2 = int(input("Digite o segundo numero: "))
if num1 == num2:
    print("Os dois numeros são iguais")
else:
    if num1 > num2:
        print("O maior numero é " + str(num1))
    else:
        print("O maior numero é " + str(num2))


